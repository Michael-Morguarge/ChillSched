namespace Shared.Model
{
    /// <summary>
    /// The date model
    /// </summary>
    public class Date
    {
        /// <summary>
        /// The string month
        /// </summary>
        public string Month { get; set; }

        /// <summary>
        /// The day
        /// </summary>
        public int Day { get; set; }

        /// <summary>
        /// The year
        /// </summary>
        public int Year { get; set; }

        /// <summary>
        /// The string day of the week
        /// </summary>
        public string DayOfWeek { get; set; }
    }
}
